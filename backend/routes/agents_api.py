@router.get("/{agent_id}/signals")
async def get_agent_signals(
    agent_id: str,
    limit: int = Query(default=20, le=50),
):
    """Get recent signals generated by a specific agent."""
    demo = await is_demo_mode()
    if demo:
        import random
        signals = []
        for i in range(min(limit, 8)):
            signals.append({
                "id": f"demo-{i}",
                "symbol": random.choice(["BTC", "ETH", "SOL"]),
                "signal_type": random.choice(["BUY", "SELL", "HOLD"]),
                "confidence": random.randint(55, 95),
                "price_at_signal": round(random.uniform(1000, 70000), 2),
                "analysis": "Demo signal — enable live mode for real analysis",
                "generated_at": (datetime.now(timezone.utc) - timedelta(minutes=i * 15)).isoformat(),
                "is_demo": True,
            })
        return {"signals": signals, "agent_id": agent_id, "demo_mode": True}

    signals = await SIGNALS_COL.find(
        {"agent_id": agent_id},
        {"_id": 0}
    ).sort("generated_at", -1).limit(limit).to_list(limit)

    # Serialize datetimes
    for s in signals:
        for k in ("generated_at", "expires_at"):
            if isinstance(s.get(k), datetime):
                s[k] = s[k].isoformat()

    return {"signals": signals, "agent_id": agent_id, "demo_mode": False}


