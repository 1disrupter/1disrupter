#  UPDATE AGENT CONFIG
# ═══════════════════════════════════════════

class AgentConfig(BaseModel):
    risk_level: Optional[str] = None
    timeframe: Optional[str] = None
    signal_frequency: Optional[str] = None
    asset: Optional[str] = None


@router.put("/{agent_id}/config")
async def update_agent_config(agent_id: str, config: AgentConfig):
    """Update agent configuration."""
    update = {}
    if config.risk_level:
        update["config.risk_level"] = config.risk_level
    if config.timeframe:
        update["config.timeframe"] = config.timeframe
    if config.signal_frequency:
        update["config.signal_frequency"] = config.signal_frequency
    if config.asset:
        update["config.asset"] = config.asset

    if not update:
        raise HTTPException(status_code=400, detail="No config fields provided")

    result = await AGENTS_COL.update_one({"id": agent_id}, {"$set": update})
    if result.matched_count == 0:
        raise HTTPException(status_code=404, detail="Agent not found")

    return {"message": "Configuration updated", "agent_id": agent_id}



