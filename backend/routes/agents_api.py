@router.get("")
async def list_agents():
    """List all agents with real-time metrics."""
    demo = await is_demo_mode()
    if demo:
        return {"agents": DEMO_AGENTS, "demo_mode": True}

    agents_cursor = AGENTS_COL.find({}, {"_id": 0}).limit(20)
    agents = await agents_cursor.to_list(20)

    now = datetime.now(timezone.utc)
    day_ago = now - timedelta(hours=24)

    enriched = []
    for a in agents:
        aid = a["id"]
        # Count signals today
        today_count = await SIGNALS_COL.count_documents({
            "agent_id": aid,
            "generated_at": {"$gte": day_ago},
        })
        # Count total signals
        total_count = a.get("total_signals", 0)
        # Accuracy: ratio of profitable signals (simplified: BUY when price went up)
        profitable = await SIGNALS_COL.count_documents({
            "agent_id": aid,
            "signal_type": {"$in": ["BUY", "SELL"]},
            "confidence": {"$gte": 70},
        })
        total_actionable = await SIGNALS_COL.count_documents({
            "agent_id": aid,
            "signal_type": {"$in": ["BUY", "SELL"]},
        })
        accuracy = round(profitable / total_actionable * 100) if total_actionable > 0 else 0

        enriched.append({
            "id": aid,
            "name": a.get("name"),
            "type": a.get("type"),
            "description": a.get("description"),
            "status": a.get("status", "active"),
            "assets": a.get("assets", []),
            "signals": today_count,
            "total_signals": total_count,
            "accuracy": accuracy,
            "last_signal_at": a.get("last_signal_at"),
            "last_run_at": a.get("last_run_at"),
            "config": a.get("config", {}),
        })

    return {"agents": enriched, "demo_mode": False}


